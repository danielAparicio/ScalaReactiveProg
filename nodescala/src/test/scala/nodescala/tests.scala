class NodeScalaSuite extends FunSuite {

  test("A Future should always be completed") {
    val always = Future.always(517)

    assert(Await.result(always, 0 nanos) == 517)
  }
  test("A Future should never be completed") {
    val never = Future.never[Int]

    try {
      //when a future doesn't finish in X second time await throws a timeout
      Await.result(never, 2 second)
      assert(false)
    } catch {
      case t: TimeoutException => // ok!
    }
  }

   test("delay") {
    //IMPORTANT:when threads are sharing  VARS we need volatile like in the Futures as futures are executed in different threads
    @volatile var test = 0;
